function incrementValueS() {
    var value = document.querySelector('#number').value;
    isNaN(value) ? 0 : value;
    // value--;
    if(value<0){
        value === 0
    }else{
        if(value>0) {
            value--
        }
    }
    document.querySelector('#number').value = value;
}
function incrementValue() {
    var value = document.querySelector('#number').value;
    isNaN(value) ? 0 : value;
    value ++
    document.querySelector('#number').value = value;
}
